class Banco
  initialize (nombre,saldo,tipoCuenta)
  @nombre = nombre
  @saldo = saldo
  @tipoCuenta = tipoCuenta
end

def nombre
  return @nombre
end

def saldo
  return @saldo
end

def tipoCuenta
  return @tipoCuenta
end

def nombre=(nombre)
  return @nombre = nombre
end

def saldo=(saldo)
  return @saldo = saldo
end

def tipoCuenta
  return @tipoCuenta = tipoCuenta
end
end

  




